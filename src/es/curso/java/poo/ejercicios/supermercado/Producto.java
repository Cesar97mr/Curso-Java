package es.curso.java.poo.ejercicios.supermercado;

public class Producto {
	private long id;
	private String nombre;
	private double precio;
	private String tipo;
	
	public Producto(long id, String nombre, double precio, String tipo) {
		super();
		this.id = id;
		this.nombre = nombre;
		this.precio = precio;
		this.tipo = tipo;
	}
	
	
	
}
